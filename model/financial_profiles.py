# ── Internal helpers ──────────────────────────────────────────────────────────

def _clip(value: float, low: float, high: float) -> float:
    return max(low, min(high, value))


def _safe_mean(series: pd.Series) -> float:
    series = pd.to_numeric(series, errors="coerce").dropna()
    return float(series.mean()) if not series.empty else 0.0


def _company_profile(rows: pd.DataFrame) -> FinancialProfile:
    rows = rows.sort_values("fiscal_year")
    company = str(rows["company_name"].iloc[0])
    latest  = rows.iloc[-1]
    first   = rows.iloc[0]
    years   = max(1, int(latest["fiscal_year"] - first["fiscal_year"]))

    latest_revenue = float(pd.to_numeric(latest.get("Revenue"), errors="coerce") or 0.0)
    first_revenue  = float(pd.to_numeric(first.get("Revenue"),  errors="coerce") or 0.0)
    if latest_revenue > 0 and first_revenue > 0 and years > 0:
        revenue_cagr = (latest_revenue / first_revenue) ** (1 / years) - 1
    else:
        revenue_cagr = 0.0

    revenue          = pd.to_numeric(rows.get("Revenue"),           errors="coerce").replace(0, pd.NA)
    operating_margin = _safe_mean(pd.to_numeric(rows.get("Operating income"),  errors="coerce") / revenue)
    fcf_margin       = _safe_mean(pd.to_numeric(rows.get("Free cash flow"),    errors="coerce") / revenue)
    capex_intensity  = _safe_mean(pd.to_numeric(rows.get("Capital expenditure"), errors="coerce").abs() / revenue)
    debt_to_assets   = _safe_mean(
        pd.to_numeric(rows.get("Total debt"),   errors="coerce")
        / pd.to_numeric(rows.get("Total assets"), errors="coerce").replace(0, pd.NA)
    )

    profitability_score = _clip(operating_margin / 0.12,   -0.5,  1.5)
    cash_score          = _clip(fcf_margin       / 0.08,   -0.5,  1.5)
    leverage_penalty    = _clip(debt_to_assets   / 0.45,    0.0,  1.5)
    capex_score         = _clip(capex_intensity  / 0.08,    0.0,  1.5)
    growth_score        = _clip(revenue_cagr     / 0.12,   -1.0,  2.0)

    resilience   = 1.0 + 0.18 * profitability_score + 0.16 * cash_score - 0.14 * leverage_penalty
    reinvestment = 1.0 + 0.12 * capex_score + 0.10 * growth_score - 0.08 * leverage_penalty

    return FinancialProfile(
        company_names=(company,),
        revenue_latest=latest_revenue,
        revenue_cagr=revenue_cagr,
        operating_margin=operating_margin,
        free_cash_flow_margin=fcf_margin,
        capex_intensity=capex_intensity,
        debt_to_assets=debt_to_assets,
        recovery_multiplier=_clip(resilience,   0.65, 1.45),
        inventory_multiplier=_clip(1.0 + 0.12 * cash_score - 0.10 * leverage_penalty, 0.75, 1.35),
        growth_multiplier=_clip(reinvestment,   0.60, 1.60),
        shock_absorption=_clip(
            0.06 * profitability_score + 0.06 * cash_score - 0.04 * leverage_penalty,
            0.0, 0.22,
        ),
    )


def _load_workbook(path: Path) -> dict[str, FinancialProfile]:
    """Load per-company profiles from a single workbook. Returns {} on failure."""
    if not path.exists():
        return {}
    try:
        financials = pd.read_excel(path, sheet_name="financials_by_year")
    except Exception:
        return {}
    if financials.empty or "company_name" not in financials.columns:
        return {}
    result: dict[str, FinancialProfile] = {}
    for company, rows in financials.groupby("company_name"):
        result[str(company)] = _company_profile(rows)
    return result


# ── Public API ────────────────────────────────────────────────────────────────

@lru_cache(maxsize=1)
def load_company_profiles() -> dict[str, FinancialProfile]:
    """
    Load and merge per-company profiles from all available financial workbooks.

    Priority: ALL_PEER_WORKBOOK (comprehensive) > DEFAULT_FINANCIAL_WORKBOOK (seed).
    A company found in the higher-priority workbook is not overwritten by the
    lower-priority one.
    """
    profiles: dict[str, FinancialProfile] = {}
    for wb_path in (ALL_PEER_WORKBOOK, DEFAULT_FINANCIAL_WORKBOOK):
        for company, profile in _load_workbook(wb_path).items():
            profiles.setdefault(company, profile)   # first hit wins
    return profiles


def combine_profiles(
    company_names: Iterable[str],
    workbook_path: str | None = None,   # kept for backward-compat; ignored
) -> FinancialProfile:
    """
    Return a revenue-weighted average profile for the given peer companies.
    Falls back to DEFAULT_PROFILE when no financial data is available.
    """
    all_profiles = load_company_profiles()
    selected = [all_profiles[n] for n in company_names if n in all_profiles]
    if not selected:
        return DEFAULT_PROFILE

    weights     = [p.revenue_latest if p.revenue_latest > 0 else 1.0 for p in selected]
    total_w     = sum(weights) or float(len(selected))

    def weighted(attr: str) -> float:
        return sum(getattr(p, attr) * w for p, w in zip(selected, weights)) / total_w

    names = tuple(n for p in selected for n in p.company_names)
    return FinancialProfile(
        company_names=names,
        revenue_latest=sum(p.revenue_latest for p in selected),
        revenue_cagr=weighted("revenue_cagr"),
        operating_margin=weighted("operating_margin"),
        free_cash_flow_margin=weighted("free_cash_flow_margin"),
        capex_intensity=weighted("capex_intensity"),
        debt_to_assets=weighted("debt_to_assets"),
        recovery_multiplier=weighted("recovery_multiplier"),
        inventory_multiplier=weighted("inventory_multiplier"),
        growth_multiplier=weighted("growth_multiplier"),
        shock_absorption=weighted("shock_absorption"),
    )


def profile_for_agent(agent_id: str) -> FinancialProfile:
    """Return the calibrated FinancialProfile for a model agent."""
    return combine_profiles(AGENT_FINANCIAL_PEERS.get(agent_id, ()))


def profile_for_tier(tier_label: str) -> FinancialProfile:
    """
    Return an aggregate FinancialProfile for all agents in a supply-chain tier.

    tier_label must be one of the keys in FOUR_TIER_AGENT_GROUPS.
    """
    agent_ids = FOUR_TIER_AGENT_GROUPS.get(tier_label, [])
    all_companies: list[str] = []
    for aid in agent_ids:
        all_companies.extend(AGENT_FINANCIAL_PEERS.get(aid, ()))
    return combine_profiles(all_companies)


def coverage_report() -> pd.DataFrame:
    """
    Return a DataFrame showing which agents have financial data and which don't.
    Useful for diagnosing missing data after running collect_all_agent_financials.py.
    """
    all_profiles = load_company_profiles()
    rows = []
    for agent_id, peers in AGENT_FINANCIAL_PEERS.items():
        matched   = [p for p in peers if p in all_profiles]
        unmatched = [p for p in peers if p not in all_profiles]
        profile   = combine_profiles(peers)
        rows.append({
            "agent_id":            agent_id,
            "peer_companies":      "; ".join(peers),
            "matched":             "; ".join(matched),
            "unmatched":           "; ".join(unmatched),
            "coverage_pct":        round(100 * len(matched) / max(len(peers), 1), 0),
            "recovery_multiplier": round(profile.recovery_multiplier, 3),
            "inventory_multiplier":round(profile.inventory_multiplier, 3),
            "growth_multiplier":   round(profile.growth_multiplier, 3),
            "shock_absorption":    round(profile.shock_absorption, 3),
        })
    return pd.DataFrame(rows)
