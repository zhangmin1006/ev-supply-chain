"""
Financial profile utilities for firm-aware agent calibration.

The model can run without any external workbook.  When financial data is
present it is loaded from (in priority order):

  1. all_peer_financials.xlsx  — comprehensive workbook covering all companies
     in AGENT_FINANCIAL_PEERS, generated by collect_all_agent_financials.py.
  2. listed_company_tiers_financials.xlsx  — seed workbook covering companies
     extracted from the supply-chain HTML dashboard.

Both workbooks must contain a "financials_by_year" sheet with at least the
columns: company_name, fiscal_year, and the financial-metric columns below.

Five-year company financials are converted to conservative multipliers:

  recovery_multiplier   — how quickly capacity recovers after a shock
  inventory_multiplier  — how much buffer stock the agent can sustain
  growth_multiplier     — capacity growth relative to baseline growth rate
  shock_absorption      — fraction of an incoming shock cushioned by
                          balance-sheet strength and cash generation

Multipliers are deliberately bounded so financial data nudges behaviour
rather than overwhelming the engineering / supply-chain calibration.
"""

